radius = input("Please enter the radius: ")
_PI = 3.14
area = _PI * int(radius)**2
print("The area of a circle is : " + str(area))

#Calculate circumference
circ = 2 * _PI * int(radius)
print("circumference of the circle is :" + str(circ))

#Calculate Diameter
diameter = 2 * int(radius)
print("Diameter of the circle is :" + str(diameter))




